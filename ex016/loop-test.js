//WHILE - LOOP SIMPLE - TEST AT THE BEGINNING
let cont = 1
while (cont <= 5) {
  console.log(`step ${cont}`)
  cont++ //same as cont = cont + 1
}


//DO WHILE - LOOP SIMPLE - TEST AT THE END
let count = 1
do {
  console.log(`step ${count}`)
  count++ //same as count = count + 1
} while (count <= 5)


//FOR - RUN, TEST, INCREMENT - RUN, TEST, INCREMENT...
for(let con = 1; con <= 5; con++ ) {
  console.log(`step ${con}`)
}


